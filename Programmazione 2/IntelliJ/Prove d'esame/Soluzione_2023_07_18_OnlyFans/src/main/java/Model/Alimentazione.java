package Model;

/**
 * Enumerazione che rappresenta i diversi tipi di alimentazione per i ventilatori.
 */
public enum Alimentazione {
    /**
     * Alimentazione a batteria.
     */
    Batteria,

    /**
     * Alimentazione tramite presa elettrica.
     */
    Presa,

    /**
     * Alimentazione meccanica.
     */
    Meccanico
}
