package Model.Tessere;

/**
 * Interfaccia che definisce i metodi comuni per tutte le tessere del gioco.
 */
public interface TesseraInterface {

    /**
     * Mostra il contenuto della tessera.
     */
    void scopri();

    /**
     * Nasconde il contenuto della tessera.
     */
    void nascondi();
}
