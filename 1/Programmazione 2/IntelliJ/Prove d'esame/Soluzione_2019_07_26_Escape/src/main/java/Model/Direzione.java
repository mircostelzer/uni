package Model;

/**
 * Enumerazione che rappresenta le possibili direzioni di movimento.
 */
public enum Direzione {

    /** Direzione verso l'alto. */
    SU,

    /** Direzione verso il basso. */
    GIU,

    /** Direzione verso destra. */
    DESTRA,

    /** Direzione verso sinistra. */
    SINISTRA,

    /** Direzione verso l'alto a destra. */
    ALTO_DESTRA,

    /** Direzione verso l'alto a sinistra. */
    ALTO_SINISTRA,

    /** Direzione verso il basso a destra. */
    BASSO_DESTRA,

    /** Direzione verso il basso a sinistra. */
    BASSO_SINISTRA
}
